#include "3-calc.h"
/**
 * op_add - add two given numbers
 * @a: the first input number
 * @b: the second input number
 * Return: the sum of 'a' and 'b'
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}
/**
 * op_sub - subtract two given numbers
 * @a: the first input number
 * @b: the second input number
 * Return: the subtract of 'a' and 'b'
 */

int op_sub(int a, int b)
{
	return ((a) - (b));
}
/**
 * op_mul - product two given numbers
 * @a: the first input number
 * @b: the second input number
 * Return: the product of 'a' and 'b'
 */

int op_mul(int a, int b)
{
	return ((a) * (b));
}
/**
 * op_div - division two given numbers
 * @a: the first input number
 * @b: the second input number
 * Return: the division of 'a' and 'b'
 */

int op_div(int a, int b)
{
	return ((a) / (b));
}
/**
 * op_mod - reminder of two given numbers division
 * @a: the first input number
 * @b: the second input number
 * Return: the reminder of divison of 'a' and 'b'
 */

int op_mod(int a, int b)
{
	return ((a) % (b));
}
